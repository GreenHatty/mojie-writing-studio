export function lessonClinic(lesson: WritingLesson): LessonClinic {
  return CLINICS[lesson.id] ?? {
    outcome: `把“${lesson.title}”转化为当前作品中可检查、可修改的具体结果。`,
    procedure: [lesson.summary, lesson.revision, `逐项核对：${lesson.checklist.join('；')}`, '修改后对照原稿，确认至少有一项状态、信息或关系发生变化。'],
    diagnosis: [lesson.mistake, ...lesson.checklist.map((item) => `无法在正文中指出“${item}”的证据`)],
    before: lesson.mistake,
    after: lesson.revision,
    whyItWorks: '判断依据落到正文证据和人物行动，不依赖抽象评价。',
    assignment: lesson.exercise.replace(/^为/u, '直接对当前作品的相关章节，为'),
    acceptance: lesson.checklist
  };
}
